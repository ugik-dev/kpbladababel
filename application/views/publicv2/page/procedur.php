<h6>
    PROSEDUR LAYANAN KANTOR PEMASRAN BERSAMA LADA PROVINSI BANGKA BELITUNG
</h6>
<p>1. Penjual/Pengusaha akan melakukan registrasi secara online di website KPB yaitu https://kpbladababel.com/
<p>2. Penjual/Pengusaha melakukan Kerjasama dengan penandatangan KSO (Kerja Sama Operasional) dengan PT.BBBS (BUMD) Provinsi Bangka Belitung.
<p> 3. Penjual/Pengusaha mendaftar sebagai Pelaku Usaha Lada Terdaftar ke KPB-LADA sesuai dengan persayaratan yang berlaku.
<p> 4. Kemudian apabila persyaratan telah terpenuhi maka KPB-Lada akan mengkonfirmasi dan mengaktifkan akun Penjual/Pengusaha Lada.
<p> 5. Penjual/Pengusaha Lada akan mengisi akun profil dan upload dokumen yang diperlukan dan mengajukan verifikasi.
<p> 6. KPB-Lada akan memeriksa kelengkapan dokumen dan memverifikasi bahwa penjual/pengusaha Lada sudah terdaftar dan bisa mengajukan permohonan pengiriman lada.
<p> 7. Penjual/Pengusaha lada dapat mengajukan permohonan pengiriman lada.
<p> 8. KPB-Lada akan memproses ke BP3L dan BPSMB untuk pengujian laboratorium dan permohonan sertifikat Indikasi Geografis (IG).
<p> 9. BPSMB akan mengupload sertifikat hasil pengujian laboratorium dan BP3L akan mengeluarkan sertifikat Indikasi Geografis (IG).
<p> 10. KPB-Lada akan memproses Surat Keterangan Transaksi bahwa lada yang dikirim sudah terverifikasi mutu dan keaslian produknya dari Provinsi Bangka Belitung.
<p> 11. Kemudian Penjual/Pengusaha dapat mengirimkan lada ke pembeli setelah memenuhi persyaratan administrasi di KPB-Lada Provinsi Bangka Belitung.
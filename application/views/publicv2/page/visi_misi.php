<h5>
    Visi
</h5>

<p>
    Mewujudkan petani lada sejahtera dan berkesinambungan dengan tata Kelola pemasaran yang berkeadilan.
</p>
<h5>
    Misi
</h5>
<p>
    1. Menjalankan sistem pasar lada dengan regulasi dan tata Kelola yang menguntungkan petani lada dan pelaku usaha.
<p>
    2. Memasarkan lada yang berkualitas berdasarkan Indikasi Geografis (IG) Muntok White Pepper (MWP).
<p>
    3. Menyelenggarakan Pasar Fisik atau Bursa Lada untuk kepentingan masyarakat.
<p>
    4. Menjalin Kerjasama yang bai kantar Petani, Pemerintah, Pengusaha dan pihak yang terkait.
<p>
    5. Memberikan pelayanan terpadu sebagai Lada Center di Provinsi Kepulauan Bangka Belitung.
</p>